import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const htmlPath = path.join(root, "web/src/index.html");
const cssPath = path.join(root, "web/dist/output.css");
const outputPath = path.join(root, "src/CU.RemoteConsole/Web/EmbeddedWebConsole.g.cs");

const html = fs.readFileSync(htmlPath, "utf8");
const css = fs.readFileSync(cssPath, "utf8");
const embedded = html.replace("/*__CU_REMOTE_CONSOLE_CSS__*/", css);

const source = `// <auto-generated />
namespace CU.RemoteConsole.Web;

internal static class EmbeddedWebConsole
{
    public const string Html = ${toVerbatimString(embedded)};
}
`;

fs.writeFileSync(outputPath, source);

function toVerbatimString(value) {
  return `@"${value.replaceAll('"', '""')}"`;
}
